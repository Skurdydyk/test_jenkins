"""Operations Module

This module provides basic arithmetic operations such as addition, subtraction,
multiplication, and division.
"""


def add(x, y):
    """Function to add two numbers.

    Parameters:
    x (int): The first number.
    y (int): The second number.

    Returns:
    int: The sum of the two numbers.
    """
    return x + y


def subtract(x, y):
    """Function to subtract one number from another.

    Parameters:
    x (int): The first number.
    y (int): The number to subtract from the first number.

    Returns:
    int: The difference between the first and second numbers.
    """
    return x - y


def multiply(x, y):
    """Function to multiply two numbers.

    Parameters:
    x (int): The first number.
    y (int): The second number.

    Returns:
    int: The product of the two numbers.
    """
    return x * y


def divide(x, y):
    """Function to divide one number by another.

    Parameters:
    x (int): The dividend.
    y (int): The divisor.

    Returns:
    float: The result of dividing the dividend by the divisor.
    """
    return x / y
